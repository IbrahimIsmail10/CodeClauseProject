    public void autoID() {
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/airline", "root", "");
            Statement s = con.createStatement();
            ResultSet rs = s.executeQuery("select MAX(CardNum) from cardtbl");
            rs.next();
            rs.getString("MAX(CardNum)");
            if (rs.getString("MAX(CardNum)") == null) {
                cardnum.setText("50607080902211");
            } else {
                long id = Long.parseLong(rs.getString("MAX(cardnum)").substring(2, rs.getString("MAX(cardnum)").length()));
                id++;
                cardnum.setText(String.format("%03d", id));
            }

        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(AddCard.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

