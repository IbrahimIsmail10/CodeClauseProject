    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed

        if ("".equals(cardnum.getText()) || "".equals(AccNum.getText()) || "".equals(defnum.getText())) {
            JOptionPane.showMessageDialog(null, "Missing Information!");
        } else {
            try {
                String card = cardnum.getText();
                String acc = AccNum.getText();
                String def = defnum.getText();
                int acc_int = Integer.parseInt(acc);
                DateFormat da = new SimpleDateFormat("yyyy-MM-dd");
                String date = da.format(exdate.getDate());
                System.out.println(date);
                Class.forName("com.mysql.jdbc.Driver");
                con = DriverManager.getConnection("jdbc:mysql://localhost/atmdb", "root", "");
                String sql = "INSERT INTO cardtbl (AccNum,CardNum,DefinationNum,ExpiredDate)VALUES(?,?,?,?)";
                pst = con.prepareStatement(sql);

                pst.setInt(1, acc_int);
                pst.setString(2, card);
                pst.setString(3, def);
                pst.setString(4, date);
                int rs = pst.executeUpdate();
                if (rs > 0) {
                    JOptionPane.showMessageDialog(null, "Card Added Successfully!");
                } else {
                    JOptionPane.showMessageDialog(null, "Failed To Add Card!");
                    AccNum.setText("");
                    cardnum.setText("");
                    defnum.setText("");
                    AccNum.requestFocus();

                }
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(AddCard.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_submitActionPerformed

